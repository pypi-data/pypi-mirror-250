def square (n :int ) -> int :
    """
    Calculates the square of a given number.

    Parameters:
        n (int): The number to be squared.

    Returns:
        int: The square of the given number.
    """
    return n*n

def addition(a :int ,b :int ) -> int :
    """
    Adds two integers and returns the sum.

    Parameters:
        a (int): The first integer.
        b (int): The second integer.

    Returns:
        int: The sum of the two integers.
    """
    return a+b

def multiplication(a :int ,b :int ) -> int :
    """
    Multiply two integers and return the result.

    Parameters:
        a (int): The first integer.
        b (int): The second integer.

    Returns:
        int: The product of the two integers.
    """
    return a*b

def subtraction(a :int ,b :int ) -> int :
    """
    Perform subtraction between two integers and return the result.

    Parameters:
        a (int): The first integer.
        b (int): The second integer.

    Returns:
        int: The result of subtracting b from a.
    """
    return a-b

def division(a :int ,b :int ) -> int :
    """
    A function that performs division between two integers and returns the result.

    :param a: An integer representing the dividend.
    :param b: An integer representing the divisor.
    :return: An integer representing the quotient of the division.
    """
    return a/b
